                                <h1>About The Name SpesMundi</h1>

                                <p>
                                    If there is thread that runs through this website I want it to be  HOPE.  (In Latin SPES) There are many challenges in our world ( in Latin MUNDI ) and in our times so many people suffer and suffer unjustly. In all these places I have been I encountered so much hope and resilience  in the face of pain and grief.  This brings me to the name of this webpage ‘SPES MUNDI’  - hope of the world.
                                </p>
                                <p>
                                    In some ways I see my life as one of Finding and Spreading Hope in our World. Thankfully I am not alone in this way of living. I feel grateful to have as friends, people of religious faith and people of no faith who are  sensitive to the suffering around them and who try to reach out  in friendship to their brothers and sisters and try to do something to bring hope comfort and change.  Many of these friends of mine have themselves experienced suffering and injustice and have inspired me with their hope. In and through all this I have found and continue to encounter the Crucified and Risen Lord, in whom my hope finds its most personal source. He is my hope – He is I believe the Hope of the World.
                                </p>
                                <p>
                                    Many years ago as a young law student on a break from reading a long legal judgement I  stubbed  quiet by chance into the religious section of the Library of University College Galway. My eye was drawn to a book called The Documents of Vatican ll. As I opened the book it fell open on a document called Gauidiam et Spes. ( Joy and Hope)  The opening paragraph continues to provide me with a ‘nutshell’ description of what the Church calls itself to be and what I seek to live out in my life.  In fact this is what the stated purpose of this document is. In this Vatican Council Document the church seeks in written form to set down ‘how it understands the presence and function of the church in the world of today.’
                                    The opening paragraph which had a deep impact on me as I stood btween the book shelves of NUIG library reads as follows:
                                    The joys and hopes, the grief and anguish of the people of our time, especially of those who are poor or afflicted, are the joys and hopes, the grief and anguish of the followers of Christ as well. Nothing that is genuinely human fails to find an echo in their hearts. For theirs is a community of people united in Christ and guided by the holy Spirit in their pilgrimage towards the Father's kingdom, bearers of a message of salvation for all of humanity. That is why they cherish a feeling of deep solidarity with the human race and its history.  ( Gaudium et Spes)
                                </p>
                                <p>
                                    For a full version Gaudium et Spes click here
                                    https://www.cctwincities.org/wp-content/uploads/2015/10/Gaudium-et-Spes-Pastoral-Constitution-on-the-Church-in-the-Modern-World.pdf
                                    This documents , written five years before I was born, nourishes my vision for my life. With the exception of this opening paragraph, many English translations I come across lack inclusive language throughout and that is a turn off in this day and age. However the one I link in this site is a good inclusive translation.   With that health warning around gender insensitive language, I still encourage you to read this document.
                                </p>
                                <p>
                                    It is over 25 years since I first read it and I still return to read it from time to time.
                                    I know it supports my desires to serve the poor and needy of this earth. It directs my efforts for reform and renewal in the Church. It is a source of hope in the face of resistance and inertia to reshape our Church. Rather than being identified as a lively community of disciples that is credibly alongside the poor and needy, the Church is for many people, especially in Ireland, otherworldly and distance.  It fails to radiate the joy and hope of the Gospel.
                                </p>
                                <p>
                                    I understand why for many people the Church is irrelevant.  Even mention of Jesus or the Gospel  can make people want to change the subject. They have had enough of Jesus Freaks or people who promote a form of Church that does not desire to journey with people in their true griefs and anguish and equally it seems to have little interest in the true joys and hopes of ordinary people. While this perception of Church and Church people and live is based in reality, it is not the full picture. The world and the Church is full of people of compassion, joy and hope and who are immersed in the mess and beauty of all this is human. In our times few of such people become the public face of the Church, ( a few do..) I refuse to let that reality distract me from where live and commitment can be found. Together as people of faith and of none we must continue the journey of witnessing to lives of love in a world marked by un-love. We must nourish our hope and the hope of future generations. We must embody spes mundi!
                                    One is right in thinking that the future of humanity rests with people who are capable of providing the generations to come with reasons for living and for hope. ( Paragraph 37)
                                </p>

